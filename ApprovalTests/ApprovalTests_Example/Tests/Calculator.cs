namespace Tests
{
    internal class Calculator
    {
        internal int Add(int p1, int p2)
        {
            return p1 + p2;
        }

        internal int Sub(int p1, int p2)
        {
            return p1 - p2;
        }

        internal int Mul(int p1, int p2)
        {
            return p1 * p2;
        }

        internal int Div(int p1, int p2)
        {
            return p1 / p2;
        }
    }
}
